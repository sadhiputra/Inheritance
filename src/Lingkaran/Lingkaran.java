public class Lingkaran {
    
      float jari=7;
      double v=3.14;
//mendefinisikan variabel
     
      public double LuasLingkaran(){
            return jari*jari*v; //kembali pada rumus luas lingkaran
      }
      public double KelilingLingkaran(){
            return (2*jari*v); //kembali pada rumus keliling lingakaran
      }

    
}
